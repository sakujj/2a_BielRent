package by.fpmibsu.bielrent.entity;

public class RentalType {
    private long id;
    private String name;

    public RentalType(){}

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
